function setFormStatus(statusBox, type, message) {
  if (!statusBox) return;
  statusBox.textContent = message;
  statusBox.classList.remove('is-success', 'is-error', 'is-loading');
  if (type) statusBox.classList.add(type);
}

async function submitLeadForm(form) {
  const statusBox = form.querySelector('.form-status');
  const submitButton = form.querySelector('button[type="submit"]');
  const messages = formMessages[currentLang] || formMessages.ru;

  setFormStatus(statusBox, 'is-loading', messages.sending);
  submitButton?.classList.add('is-submitting');
  if (submitButton) submitButton.disabled = true;

  const payload = Object.fromEntries(new FormData(form).entries());

  try {
    const response = await fetch(form.action, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(payload),
    });

    const result = await response.json().catch(() => ({}));

    if (response.ok && result.ok) {
      setFormStatus(statusBox, 'is-success', messages.success);
      form.reset();
      return;
    }

    setFormStatus(statusBox, 'is-error', result.message || messages.error);
  } catch (error) {
    setFormStatus(statusBox, 'is-error', messages.network);
    console.error(error);
  } finally {
    submitButton?.classList.remove('is-submitting');
    if (submitButton) submitButton.disabled = false;
  }
}

document.querySelectorAll('.lead-capture-form').forEach((form) => {
  form.addEventListener('submit', async (e) => {
    e.preventDefault();
    await submitLeadForm(form);
  });
});
