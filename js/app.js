const modal = document.getElementById('galleryModal');
const modalImg = document.getElementById('galleryImage');
const modalCaption = document.getElementById('galleryCaption');
const closeBtn = document.getElementById('galleryClose');
const prevBtn = document.getElementById('galleryPrev');
const nextBtn = document.getElementById('galleryNext');
let currentGallery = [];
let currentGalleryKey = '';
let currentIndex = 0;
let touchStartX = 0;

function renderGallery(){
  if (!modalImg || !modalCaption || !currentGallery.length) return;
  modalImg.src = currentGallery[currentIndex];
  modalCaption.textContent = `${galleryTitles[currentLang][currentGalleryKey]} — ${currentIndex+1}/${currentGallery.length}`;
}

function openGallery(key){
  if (!galleries[key] || !galleries[key].length) return;
  currentGalleryKey = key;
  currentGallery = galleries[key];
  currentIndex = 0;
  renderGallery();
  modal.classList.add('open');
  modal.setAttribute('aria-hidden','false');
}

function closeGallery(){
  modal.classList.remove('open');
  modal.setAttribute('aria-hidden','true');
}

function stepGallery(direction){
  if (!currentGallery.length) return;
  currentIndex = (currentIndex + direction + currentGallery.length) % currentGallery.length;
  renderGallery();
}

document.querySelectorAll('.open-gallery').forEach((btn) => {
  btn.addEventListener('click', (e) => {
    e.preventDefault();
    openGallery(btn.dataset.gallery);
  });
});

if (closeBtn) {
  closeBtn.addEventListener('click', (e) => {
    e.preventDefault();
    e.stopPropagation();
    closeGallery();
  });
  closeBtn.addEventListener('touchstart', (e) => {
    e.preventDefault();
    e.stopPropagation();
    closeGallery();
  }, { passive: false });
}

if (prevBtn) {
  prevBtn.addEventListener('click', (e) => {
    e.preventDefault();
    e.stopPropagation();
    stepGallery(-1);
  });
  prevBtn.addEventListener('touchstart', (e) => {
    e.preventDefault();
    e.stopPropagation();
    stepGallery(-1);
  }, { passive: false });
}

if (nextBtn) {
  nextBtn.addEventListener('click', (e) => {
    e.preventDefault();
    e.stopPropagation();
    stepGallery(1);
  });
  nextBtn.addEventListener('touchstart', (e) => {
    e.preventDefault();
    e.stopPropagation();
    stepGallery(1);
  }, { passive: false });
}

if (modal) {
  modal.addEventListener('click', (e) => {
    if (e.target === modal) closeGallery();
  });
}

if (modalImg) {
  modalImg.addEventListener('touchstart', (e) => {
    touchStartX = e.changedTouches[0].clientX;
  }, { passive: true });

  modalImg.addEventListener('touchend', (e) => {
    const touchEndX = e.changedTouches[0].clientX;
    const deltaX = touchEndX - touchStartX;
    if (Math.abs(deltaX) < 40) return;
    if (deltaX < 0) stepGallery(1);
    else stepGallery(-1);
  }, { passive: true });
}

document.addEventListener('keydown', (e)=>{
  if(!modal || !modal.classList.contains('open')) return;
  if(e.key==='Escape') closeGallery();
  if(e.key==='ArrowRight') stepGallery(1);
  if(e.key==='ArrowLeft') stepGallery(-1);
});

